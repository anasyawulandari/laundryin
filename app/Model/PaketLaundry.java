package laundryin;

/**
 *
 * @author IAM
 */
public class PaketLaundry {
    private String namaPaketLaundry;
    private String jenisPaket;
    private String kategoriBarang;
    private int jumlahBarang;

    public String getNamaPaketLaundry() {
        return namaPaketLaundry;
    }

    public void setNamaPaketLaundry(String namaPaketLaundry) {
        this.namaPaketLaundry = namaPaketLaundry;
    }

    public String getJenisPaket() {
        return jenisPaket;
    }

    public void setJenisPaket(String jenisPaket) {
        this.jenisPaket = jenisPaket;
    }

    public String getKategoriBarang() {
        return kategoriBarang;
    }

    public void setKategoriBarang(String kategoriBarang) {
        this.kategoriBarang = kategoriBarang;
    }

    public int getJumlahBarang() {
        return jumlahBarang;
    }

    public void setJumlahBarang(int jumlahBarang) {
        this.jumlahBarang = jumlahBarang;
    }
    
}
